function double(n) {
  return n * 2;
}

function square(n) {
  return n * n;
}

function isEven(n) {
  return n % 2 === 0;
}

function isOdd(n) {
  return n % 2 !== 0;
}

function multiply(a, b) {
  return a * b;
}

console.log(double(5));
console.log(square(4));
console.log(isEven(8));
console.log(isOdd(7));
console.log(multiply(3, 6));